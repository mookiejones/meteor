function getNum(v){
  return Math.floor(Math.random() * parseInt(v));
}
function getRandomDate(from, to) {
    if (!from) {
        from = new Date(2011, 1).getTime();
    } else {
        from = from.getTime();
    }
    if (!to) {
        to = new Date(2015,0, 1).getTime();
    } else {
        to = to.getTime();
    }
    var date = new Date(from + Math.random() * (to-from));

    var result = date.getMonth() + '/' + date.getDate() + '/' + date.getFullYear();
    return result;
}
